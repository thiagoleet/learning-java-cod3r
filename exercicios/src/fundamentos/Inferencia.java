package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		double a = 4.5;
		System.out.println(a);

		var b = 12;
		System.out.println(b);

		var c = "This is a text";
		System.out.println(c);

		c = "This is an another text";
		System.out.println(c);

		double d; // a variável foi declarada
		d = 123.65; // a variável foi inicializada
		System.out.println(d); // a variável foi usada
		

	}
}
